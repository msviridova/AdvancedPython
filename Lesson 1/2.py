def fibonacci(num):
    fib_row = [num, (num-1) + num]
    while len(fib_row) < 10:
        fib_num_1 = fib_row[-1]
        fib_num_2 = fib_row[-2]
        fib_row.append(fib_num_1 + fib_num_2)

    print(*fib_row, sep=", ")


print(fibonacci(0))


